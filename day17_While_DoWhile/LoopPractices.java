package day17_While_DoWhile;

public class LoopPractices {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.print(i);

        }

        System.out.println("____________");

        int j=1;

        while (j<=10){
            j++;

            System.out.print(j);
        }

        System.out.println("_________________");
    }
}
