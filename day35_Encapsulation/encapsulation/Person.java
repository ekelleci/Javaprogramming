package day35_Encapsulation.encapsulation;

public class Person {
    // getters and setters example
    // example private data
    private String name;
    private int age;

    // getter
    public String getName(){
        return name;
    }

    public int getAge(){
        return age;
    }
    // setter
    public void setName(String name){
        this.name=name;
    }

    public void setAge(int age){
        this.age=age;
    }




}
